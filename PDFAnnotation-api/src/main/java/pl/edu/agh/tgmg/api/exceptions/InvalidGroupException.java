package pl.edu.agh.tgmg.api.exceptions;

public class InvalidGroupException extends InvalidAnnotationException {

	public InvalidGroupException() {
		super();
	}

	public InvalidGroupException(String message, Throwable cause) {
		super(message, cause);
	}

	public InvalidGroupException(String message) {
		super(message);
	}

	public InvalidGroupException(Throwable cause) {
		super(cause);
	}

	
}
